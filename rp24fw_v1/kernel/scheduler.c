/* 優先度ごとのReadyキューの配列 */
TCB *ready_queue[CNF_MAX_TSKPRI];

/* 資源占用中のタスク */
TCB *cur_task;

/* 次のタスク */
TCB *sche_task;

u4 disp_running;

/********************************************************/
/* 内部公開変数                                         */
/********************************************************/

/********************************************************/
/* 外部公開関数定義                                     */
/********************************************************/

/********************************************************/
/* 関数   | void scheduler( void )                      */
/* 説明   | タスクのスケジューリング                    */
/* 引数   | なし                                        */
/* 戻り値 | なし                                        */
/********************************************************/
void scheduler( void )
{
    INT i;

    for(i = 0; i < CNF_MAX_TSKPRI; i++)
    {
        if(ready_queue[i] != NULL)
        {
            break;
        }
    }

    if(i < CNF_MAX_TSKPRI)
    {
        sche_task = ready_queue[i];
    }
    else
    {
        sche_task = NULL;
    }

    if((sche_task != cur_task)
      &&(!disp_running))
    {
        dispatch();
    }
}

